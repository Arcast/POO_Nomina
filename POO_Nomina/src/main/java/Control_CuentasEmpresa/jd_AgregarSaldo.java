    public void ModificaSaldo(){
        try {
        String NombreBanco, NumeroCuenta, Evento;
        Double Monto; double Credito = 0; double Debito = 0;
               
        if (Double.parseDouble(jspAgregarSaldo.getValue().toString()) < 0 ) {
            JOptionPane.showMessageDialog(this, "ingrese un monto mayor a cero", "NotificaciÃ³n" , JOptionPane.INFORMATION_MESSAGE);
            jspAgregarSaldo.setValue(0);
            return;
        }        
                        
        NombreBanco = txtBanco.getText().toString();
        NumeroCuenta= txtCuenta.getText().toString();
        Evento = cbxCuenta.getSelectedItem().toString();
        Monto = Double.parseDouble(jspAgregarSaldo.getValue().toString());
                     
        if (Evento.equals("Sumar")) {
            Credito = Monto;
        }else{
            
            double SaldoActual = Double.parseDouble(SumarSaldo(NumeroCuenta));
            
             if ((SaldoActual - Monto) < 0) {
                JOptionPane.showMessageDialog(this, "Monto ingresado no permitido debido a que el saldo queda negativo", "Error" , JOptionPane.ERROR_MESSAGE);
                return;
             }            
            Debito = Monto;
        }                       
             
        if (JOptionPane.showConfirmDialog(this, "Desea agregar el nuevo movimiento?", "WARNING", JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION) {
          return;
        }
        
        MovimientosCuentas movimientos = new MovimientosCuentas(NumeroCuenta, String.valueOf(Credito), String.valueOf(Debito));
        
        MovimientosCuentasDAO movimientosCuentasDAO = new MovimientosCuentasDAO();
        movimientosCuentasDAO.CrearMovimientoCuentas(movimientos);
                      
        JOptionPane.showMessageDialog(this, "Movimiento generado exitosamente", "NotificaciÃ³n" , JOptionPane.INFORMATION_MESSAGE);
        
        limpiar();
        MostrarSaldos();
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error al actualizar el Empleado", "Error" , JOptionPane.ERROR_MESSAGE);
            return;
        }
    }
    
      public void MostrarSaldos(){
        try {                  
            List<Cuentas> listaCuentas = new ArrayList<>();   
            CuentasDAO cuentasDAO = new CuentasDAO(); 
            listaCuentas = cuentasDAO.leerCuentas(); 
                      
            
            //Limpieza de la tabla
            int filas = modelo.getRowCount();
            for (int a = 0; filas > a; a++) {
                modelo.removeRow(0);
            }    
            
            for (Cuentas cuenta : listaCuentas) {               
                
                NombreBanco = cuenta.getNombreBanco().toString();
                NumeroCuenta = cuenta.getCuenta().toString();        
                
                String Saldo = SumarSaldo(NumeroCuenta);
                
                modelo.addRow(new Object[]{NombreBanco, NumeroCuenta, Saldo});
            }
                                    
                   
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error al cargar usuarios", "Error" , JOptionPane.ERROR_MESSAGE);
            return;
        }
    }
      
     public String SumarSaldo(String NumeroCuenta){
         
         try {
             
            List<MovimientosCuentas> listaMovimientos = new ArrayList<>();   
            MovimientosCuentasDAO movimientosCuentasDAO = new MovimientosCuentasDAO(); 
            listaMovimientos = movimientosCuentasDAO.leerMovimientosCuentas(); 
            
            double Creditos = 0.0; double Debitos = 0.0; double Sumatoria = 0.0;
            String Saldo;
            
            for (MovimientosCuentas movimientos : listaMovimientos) {               
                if (movimientos.getNumeroCuenta().equals(NumeroCuenta)) {
                    Creditos += Double.parseDouble(movimientos.getCreditos());
                    Debitos += Double.parseDouble(movimientos.getDebitos());
                }
            }
            
            Sumatoria = Creditos - Debitos;               
            Saldo = String.valueOf(Sumatoria);
                      
            return Saldo;
             
         } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Ha ocurrido un error al calcular el saldo", "Error" , JOptionPane.ERROR_MESSAGE);
            return "";
         }
     }
   
    
